/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class PluginAction extends XmobileAction
{
//	var $template = 'xmobile_plugin.html';
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	function PluginAction()
	{
	}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	function setTemplate()
	{
		$this->controller->render->setTemplate($this->pluginHandler->template);
	}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	function setTitle()
	{
		$this->controller->render->setTitle($this->pluginHandler->getModuleName());
		$this->controller->render->template->assign('page_title',$this->pluginHandler->getModuleName());
		if ($this->controller->getViewState() != 'default')
		{
			$this->controller->render->template->assign('page_title_link',$this->pluginHandler->getTitleLink());
		}

		global $xoopsModuleConfig;
		$this->controller->render->template->assign('use_accesskey',$xoopsModuleConfig['use_accesskey']);
		$this->controller->render->template->assign('show_item_count',$xoopsModuleConfig['show_item_count']);
	}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// 初期画面
	function getDefaultView()
	{
//		return $this->executePluginView();
		$this->executePluginView();
	}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// 一覧画面
	function getListView()
	{
		return $this->executePluginView();
	}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// 詳細画面
	function getDetailView()
	{
		return $this->executePluginView();
	}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// 編集画面
	function getEditView()
	{
		return $this->executePluginView();
	}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// 投稿画面
	function getConfirmView()
	{
		return $this->executePluginView();
	}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}
